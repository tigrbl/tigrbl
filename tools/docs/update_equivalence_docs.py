def _engine_order(engines: Iterable[str]) -> list[str]:
    preferred = ["sqlite", "postgres", "dataframe", "file", "cache"]
    engine_set = set(engines)
    return [engine for engine in preferred if engine in engine_set] + sorted(
        engine_set - set(preferred)
    )


def render_datatype_lowering_matrix() -> str:
    _add_workspace_paths()
    from tigrbl_core._spec.datatypes import (  # noqa: PLC0415
        CANONICAL_DATATYPES,
        DataTypeSpec,
        EngineDatatypeBridge,
        EngineRegistry,
    )

    registry = EngineRegistry()
    bridge = EngineDatatypeBridge(registry)
    engines = _engine_order(registry.known_engines())
    rows = []
    for logical_name in CANONICAL_DATATYPES:
        row = [_code(logical_name)]
        for engine in engines:
            try:
                lowered = bridge.lower(engine, DataTypeSpec(logical_name), strict=True)
                row.append(_code(lowered.physical_name))
            except LookupError:
                row.append("not mapped")
        rows.append(row)

    headers = ["Logical datatype"] + [engine.title() for engine in engines]
    return "\n\n".join(
        [
            _generated_note(
                ["pkgs/core/tigrbl_core/tigrbl_core/_spec/datatypes.py"]
            ),
            _markdown_table(headers, rows),
        ]
    )


def render_reflection_hints() -> str:
    _add_workspace_paths()
    from tigrbl_core._spec import datatypes as datatype_mod  # noqa: PLC0415

    hints = getattr(datatype_mod, "_REFLECTION_HINTS")
    rows = []
    for engine_key in [None, "sqlite", "postgres"]:
        mapping = hints.get(engine_key, {})
        by_logical: dict[str, list[str]] = {}
        for physical_name, logical_hint in sorted(mapping.items()):
            by_logical.setdefault(logical_hint, []).append(physical_name)
        label = "generic" if engine_key is None else str(engine_key)
        for logical_hint in sorted(by_logical):
            rows.append(
                [
                    _code(label),
                    _code_list(by_logical[logical_hint]),
                    _code(logical_hint),
                ]
            )

    return "\n\n".join(
        [
            _generated_note(
                ["pkgs/core/tigrbl_core/tigrbl_core/_spec/datatypes.py"]
            ),
            _markdown_table(["Engine family", "Physical names", "Logical hint"], rows),
        ]
    )


