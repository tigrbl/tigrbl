def _wrap_block(block_id: str, body: str) -> str:
    return (
        f"{BEGIN_TEMPLATE.format(block_id=block_id)}\n"
        f"{body.rstrip()}\n"
        f"{END_TEMPLATE.format(block_id=block_id)}"
    )


def _replace_block(text: str, block_id: str, replacement: str) -> tuple[str, bool]:
    begin = BEGIN_TEMPLATE.format(block_id=block_id)
    end = END_TEMPLATE.format(block_id=block_id)
    pattern = re.compile(
        re.escape(begin) + r".*?" + re.escape(end),
        re.DOTALL,
    )
    new_text, count = pattern.subn(replacement, text, count=1)
    return new_text, count == 1


def _expected_text(path: Path, blocks: list[GeneratedBlock]) -> tuple[str | None, list[str]]:
    errors: list[str] = []
    text = path.read_text(encoding="utf-8")
    updated = text
    for block in blocks:
        replacement = _wrap_block(block.block_id, block.render())
        updated, found = _replace_block(updated, block.block_id, replacement)
        if not found:
            errors.append(
                f"{path.relative_to(ROOT).as_posix()} missing generated block {block.block_id}"
            )
    if errors:
        return None, errors
    return updated, []


def check_docs() -> list[str]:
    errors: list[str] = []
    blocks_by_path: dict[Path, list[GeneratedBlock]] = {}
    for block in generated_blocks():
        blocks_by_path.setdefault(block.path, []).append(block)

    for path, blocks in sorted(blocks_by_path.items()):
        expected, path_errors = _expected_text(path, blocks)
        errors.extend(path_errors)
        if expected is None:
            continue
        current = path.read_text(encoding="utf-8")
        if current != expected:
            diff = "\n".join(
                difflib.unified_diff(
                    current.splitlines(),
                    expected.splitlines(),
                    fromfile=f"{path.relative_to(ROOT).as_posix()} (current)",
                    tofile=f"{path.relative_to(ROOT).as_posix()} (generated)",
                    lineterm="",
                    n=3,
                )
            )
            errors.append(
                f"{path.relative_to(ROOT).as_posix()} generated equivalence block is stale.\n{diff}"
            )
    return errors


def write_docs() -> list[str]:
    errors: list[str] = []
    changed: list[str] = []
    blocks_by_path: dict[Path, list[GeneratedBlock]] = {}
    for block in generated_blocks():
        blocks_by_path.setdefault(block.path, []).append(block)

    for path, blocks in sorted(blocks_by_path.items()):
        expected, path_errors = _expected_text(path, blocks)
        errors.extend(path_errors)
        if expected is None:
            continue
        current = path.read_text(encoding="utf-8")
        if current != expected:
            path.write_text(expected, encoding="utf-8")
            changed.append(path.relative_to(ROOT).as_posix())

    if changed:
        print("Updated generated equivalence docs:")
        for item in changed:
            print(f"- {item}")
    else:
        print("Generated equivalence docs are current.")
    return errors


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Update or verify generated sections in equivalence docs."
    )
    mode = parser.add_mutually_exclusive_group(required=True)
    mode.add_argument("--check", action="store_true", help="Fail if generated blocks are stale.")
    mode.add_argument("--write", action="store_true", help="Rewrite generated blocks in place.")
    args = parser.parse_args()

    errors = write_docs() if args.write else check_docs()
    if errors:
        for error in errors:
            print(error, file=sys.stderr)
        raise SystemExit(1)
    if args.check:
        print("generated equivalence docs validation passed")


if __name__ == "__main__":
    main()
