def _parse_engine_entry_points(pyproject: Path) -> list[str]:
    text = pyproject.read_text(encoding="utf-8")
    section = re.search(
        r'(?ms)^\[project\.entry-points\."tigrbl\.engine"\]\s*(.*?)(?=^\[|\Z)',
        text,
    )
    if section is None:
        return []
    entries: list[str] = []
    for line in section.group(1).splitlines():
        match = re.match(r"\s*([A-Za-z0-9_.-]+)\s*=\s*\"[^\"]+\"", line)
        if match:
            entries.append(match.group(1))
    return sorted(entries)


def _parse_project_name(pyproject: Path) -> str:
    text = pyproject.read_text(encoding="utf-8")
    match = re.search(r'(?m)^name\s*=\s*"([^"]+)"', text)
    return match.group(1) if match else pyproject.parent.name


