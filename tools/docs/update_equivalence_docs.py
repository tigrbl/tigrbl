ROOT = Path(__file__).resolve().parents[2]

BEGIN_TEMPLATE = "<!-- BEGIN GENERATED: {block_id} -->"
END_TEMPLATE = "<!-- END GENERATED: {block_id} -->"


@dataclass(frozen=True)
class GeneratedBlock:
    path: Path
    block_id: str
    render: Callable[[], str]


def _add_workspace_paths() -> None:
    candidates = [ROOT, ROOT / "examples" / "equivalence_contracts" / "src"]
    for base in (ROOT / "pkgs" / "core", ROOT / "pkgs" / "apps", ROOT / "pkgs" / "engines"):
        if not base.is_dir():
            continue
        for child in sorted(base.iterdir()):
            if not child.is_dir():
                continue
            candidates.append(child)
            src = child / "src"
            if src.is_dir():
                candidates.append(src)

    for path in reversed(candidates):
        text = str(path)
        if text not in sys.path:
            sys.path.insert(0, text)


def _code(value: str) -> str:
    return f"`{value}`"


def _code_list(values: Iterable[str], *, none: str = "none") -> str:
    items = [str(value) for value in values]
    if not items:
        return none
    return ", ".join(_code(item) for item in items)


def _markdown_table(headers: list[str], rows: list[list[str]]) -> str:
    out = [
        "| " + " | ".join(headers) + " |",
        "| " + " | ".join("---" for _ in headers) + " |",
    ]
    out.extend("| " + " | ".join(row) + " |" for row in rows)
    return "\n".join(out)


def _generated_note(sources: Iterable[str]) -> str:
    source_text = ", ".join(_code(source) for source in sources)
    return (
        "Generated by `tools/docs/update_equivalence_docs.py`. "
        "Edit the source inputs, then run "
        "`python tools/docs/update_equivalence_docs.py --write`.\n\n"
        f"Source inputs: {source_text}."
    )


def _format_status(missing: list[str]) -> str:
    if not missing:
        return "present"
    return "missing: " + _code_list(missing)


def _facade_exports() -> set[str]:
    facade = ROOT / "pkgs" / "core" / "tigrbl" / "tigrbl" / "__init__.py"
    tree = ast.parse(facade.read_text(encoding="utf-8"))
    exports: set[str] = set()
    for node in tree.body:
        if not isinstance(node, ast.Assign):
            continue
        if not any(isinstance(target, ast.Name) and target.id == "__all__" for target in node.targets):
            continue
        if not isinstance(node.value, (ast.List, ast.Tuple)):
            continue
        for item in node.value.elts:
            if isinstance(item, ast.Constant) and isinstance(item.value, str):
                exports.add(item.value)
    return exports


def _public_surface_rows(groups: list[tuple[str, list[str]]]) -> list[list[str]]:
    exported = _facade_exports()
    rows: list[list[str]] = []
    for label, symbols in groups:
        missing = [symbol for symbol in symbols if symbol not in exported]
        rows.append([label, _code_list(symbols), _format_status(missing)])
    return rows


